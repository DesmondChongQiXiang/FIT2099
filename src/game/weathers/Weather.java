package game.weathers;

/**
 * The Weather enum represents different weather conditions in the game.
 * It includes two possible weather conditions: SUNNY and RAINY.
 * This enum is used to model the weather in the game world and can affect gameplay.
 *
 * @author MA_AppliedSession1_Group7
 */
public enum Weather {
    /**
     * Represents a sunny weather condition in the game.
     * Sunny weather typically has certain effects or consequences in the game world.
     */
    SUNNY,

    /**
     * Represents a rainy weather condition in the game.
     * Rainy weather typically has different effects or consequences compared to sunny weather.
     */
    RAINY
}

